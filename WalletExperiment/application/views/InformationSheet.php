            <h6>STUDY PURPOSE</h6>

            <p>

	   	The purpose of this study is to gather information regarding your app choices in certain categories in a simulation of Android Play Store.
            </p>

​

            <h6>PROCEDURES FOR THE STUDY</h6>

            <p>

	    	Upon agreeing to participate in this study you will be asked to install apps from a simulated Android Play Store and answer some survey questions. You are asked to choose apps in a simulation of Android Play Store as if you wanted to install an app from that category on your phone. No apps will actually be installed on your device as this experiment is only a simulation. You need to install 4 apps from the 8 apps shown to you in each category. You can click on apps' image to see the app description and decide if you want to install the app or not.<b> During the study make sure not to use your browser's back button and instead, use the back button in the simulation section. If you click on your browser's back button the experiment will be restarted.</b>

            </p>

​

            <p>

​

                You should be able to complete the study in approximately 5-15 minutes.

​

            </P>

